# Leia as medidas de um terreno retangular
# (comprimento e largura) e mostre a àrea e o
# perímetro do terreno.

compr = float(input("Solicite o comprimento do terreno: "))
largu = float(input("Solicite a largura do terreno: "))
print("Area: {}".format((compr * largu)))
print("Perimetro: {}".format((compr * largu) * 2))
